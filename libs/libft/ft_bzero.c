#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *p;

	if (n > 0)
	{
		p = (unsigned char *)s;
		while (n > 0)
		{
			*p = '\0';
			p++;
			n--;
		}
	}
}
