#include "libft.h"

size_t	ft_itod(int nbr)
{
	size_t	digits;

	digits = 0;
	if (nbr == 0)
		return (1);
	while (nbr)
	{
		nbr /= 10;
		digits++;
	}
	return (digits);
}
