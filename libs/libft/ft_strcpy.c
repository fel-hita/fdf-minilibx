#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*p_dst;

	p_dst = dst;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (p_dst);
}
